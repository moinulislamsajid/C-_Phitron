#include<iostream>

using namespace std;

int main(){


    int num;

    cout<<"Enter any value(1-5) : ";
    cin>>num;

    switch(num){

    case 1:
        cout<<"One";
        break;

    case 2:
        cout<<"Two";
        break;

    case 3:
        cout<<"Three";
        break;

    case 4:

        cout<<"Four";
        break;

    case 5:

        cout<<"Five";
        break;

    default :

        cout<<"Please enter value (1-6)";
        break;



    }




}
