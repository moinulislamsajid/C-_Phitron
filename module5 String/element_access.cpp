#include<bits/stdc++.h>

using namespace std;

int main()
{

    string name = "Ayman Sajid";

    // access the value at first using array

    cout<<name[0]<<endl;

    cout<<name.at(0)<<endl;

    // access the last element of the arrays

    cout<<name[name.size()-1]<<endl;

    // using in build function using back() function

    cout<<name.back()<<endl;

    // if you see first index then you can use front();

    cout<<name.front()<<endl;



}
